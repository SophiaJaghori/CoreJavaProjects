public class Main {
	public static void main(String[] args) {
		
		// Implicit type casting
		byte age = 30;
		int x = age;
		System.out.println("your age is  "+ age);
		
		//Explicit type casting
		int age1 = 20;
		byte y = (byte)age1;
		System.out.println("your age is "+ age);
		float i = 1.25f;
		double j = i;
		double k = 2.25;
		float f = (float)k;
		
	}

}
